function updateSummary(data) {
  const total = data.length;
  const avgPrice = total ? Math.round(data.reduce((sum, item) => sum + item.price, 0) / total) : 0;
  const avgRating = total ? (data.reduce((sum, item) => sum + item.ratings, 0) / total).toFixed(2) : 0;
  const maxBattery = data.length ? Math.max(...data.map(item => item.battery)) : 0;
  document.getElementById('statTotal').textContent = total;
  document.getElementById('statPrice').textContent = formatRupiah(avgPrice);
  document.getElementById('statRating').textContent = avgRating;
  document.getElementById('statBattery').textContent = `${maxBattery}mAh`;
}

function applyFilters() {
  const search = document.getElementById('searchInput').value.toLowerCase().trim();
  const display = document.getElementById('filterDisplay').value;
  const storage = document.getElementById('filterStorage').value;
  const ram = document.getElementById('filterRam').value;
  const android = document.getElementById('filterAndroid').value;
  const processor = document.getElementById('filterProcessor').value.toLowerCase();
  const sortBy = document.getElementById('sortSelect').value;
  let results = mobileData.filter(item => {
    const matchesSearch = search ? item.name.toLowerCase().includes(search) : true;
    const matchesDisplay = display === 'all' ? true : item.display === display;
    const matchesStorage = storage === 'all' ? true : item.storage === storage;
    const matchesRam = ram === 'all' ? true : item.ram === ram;
    const matchesAndroid = android === 'all' ? true : item.android_version === android;
    const matchesProcessor = processor ? item.processor.toLowerCase().includes(processor) : true;
    return matchesSearch && matchesDisplay && matchesStorage && matchesRam && matchesAndroid && matchesProcessor;
  });
  if (sortBy === 'price-asc') results.sort((a,b) => a.price - b.price);
  if (sortBy === 'price-desc') results.sort((a,b) => b.price - a.price);
  if (sortBy === 'rating-desc') results.sort((a,b) => b.ratings - a.ratings);
  if (sortBy === 'battery-desc') results.sort((a,b) => b.battery - a.battery);
  if (sortBy === 'camera-desc') results.sort((a,b) => (Number(b.camera)||0) - (Number(a.camera)||0));
  state.filtered = results;
  document.getElementById('resultCount').textContent = results.length;
  renderCards(results.slice(0, 60));
  updateSummary(results);
  updateCharts(results);
}

